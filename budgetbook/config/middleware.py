import logging
import secrets
import threading
import time
from collections import deque

from django.conf import settings
from django.http import JsonResponse

CSP_NONCE_PLACEHOLDER = '__CSP_NONCE__'

_rate_limit_logger = logging.getLogger('budgetbook.security')


class CSPNonceMiddleware:
    """Per-request CSP nonce. Stored on request.csp_nonce so templates and
    response middleware can pick it up."""

    def __init__(self, get_response):
        self.get_response = get_response

    def __call__(self, request):
        request.csp_nonce = secrets.token_urlsafe(16)
        return self.get_response(request)


class ContentSecurityPolicyMiddleware:
    """Attach the configured Content-Security-Policy header.

    `__CSP_NONCE__` placeholders are substituted with the per-request nonce
    generated by CSPNonceMiddleware.
    """

    def __init__(self, get_response):
        self.get_response = get_response

    def __call__(self, request):
        response = self.get_response(request)
        if settings.CSP_ENABLED and 'Content-Security-Policy' not in response:
            policy = settings.CONTENT_SECURITY_POLICY
            nonce = getattr(request, 'csp_nonce', '')
            if nonce and CSP_NONCE_PLACEHOLDER in policy:
                policy = policy.replace(CSP_NONCE_PLACEHOLDER, nonce)
            response['Content-Security-Policy'] = policy
        return response


def csp_nonce(request):
    """Context processor exposing request.csp_nonce to templates."""
    return {'csp_nonce': getattr(request, 'csp_nonce', '')}


def static_version(request):
    """Context processor exposing STATIC_VERSION to templates as cache buster."""
    return {'STATIC_VERSION': getattr(settings, 'STATIC_VERSION', '')}


